function reduceOpacityOne() {
    $(function() {
        $("#one").animate({
            opacity: '-=0.5'
        });
     });
}


function increaseOpacityOne() {
 $(function() {
    $("#one").animate({
        opacity: '+=0.5'
    });
 });
}


